public class Assignment_Equal_Operators {
    public static void main(String[] args)
    {
        //this is a code segment  to demonstrate the main difference between the equal and assignment operator
        String CarName="BMW";
        if(CarName=="BMW")
        {
            System.out.println("Hey! You Got a BMW");
        }

        else

        {
            System.out.println("Lets Work on Getting a BMW ");
        }

        //This is  an example of use of assignment operator
        boolean car = true;
        if (car=true)
        {
            System.out.printf("Congratulations on your new  car");
        }
    }
}
